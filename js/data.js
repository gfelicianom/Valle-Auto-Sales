/* Shown only if inventory.json is missing or empty. Kept empty on
   purpose — an empty lot message beats fake cars. */
const SAMPLE_CARS = [];

async function loadInventory() {
  try {
    const res = await fetch("js/inventory.json", { cache: "no-cache" });
    if (!res.ok) throw new Error("HTTP " + res.status);
    const data = await res.json();
    /* fill defaults so the rest of the site can assume every key exists */
    const cars = (Array.isArray(data.cars) ? data.cars : [])
      .filter(c => c && c.id && (c.make || c.title))
      .map(c => ({
        title: "", make: "", model: "", year: 0, color: "", mileage: 0,
        price: 0, body_type: "other", engine_liters: 0, cylinders: 0,
        drivetrain: "", fuel_type: "", origin: "", registration_fee: "",
        condition_tags: [], photo_urls: [], featured: false, sold: false,
        notes: "", ...c
      }));
    return cars.length ? cars : SAMPLE_CARS;
  } catch (e) {
    console.warn("Could not load js/inventory.json, using sample data.", e);
    return SAMPLE_CARS;
  }
}
